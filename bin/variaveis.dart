void main(){

  //Variável do tipo int armazena um valor inteiro
  int idade = 21;

  //Variável do tipo double armazena um valor fracionado, EX: 2.89
  double altura = 1.67;

  //Variável do tipo bool trabalha com dois valores que é verdadeiro = true ou falso = false
  bool geek = false;

  //Variável do tipo String armazena letra, palavra, texto e por ai vai
  String nome = 'Wesley';

  print('Nome: $nome\nIdade: $idade\n'
  'Altura: $altura\nSou geek? $geek');

}

